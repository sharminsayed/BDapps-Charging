package com.sharmin.charging.bdapps;

public class Robi {
    private String APP_ID = "";
    private String APP_PASSWORD = "";
    private String APP_PATH = "";
    private String MSG_TEXT = "";
    private String WINDOW_TEXT = "";

    public String getAPP_ID() {
        return APP_ID;
    }

    public void setAPP_ID(String APP_ID) {
        this.APP_ID = APP_ID;
    }

    public String getAPP_PASSWORD() {
        return APP_PASSWORD;
    }

    public void setAPP_PASSWORD(String APP_PASSWORD) {
        this.APP_PASSWORD = APP_PASSWORD;
    }

    public String getAPP_PATH() {
        return APP_PATH;
    }

    public void setAPP_PATH(String APP_PATH) {
        this.APP_PATH = APP_PATH;
    }

    public String getMSG_TEXT() {
        return MSG_TEXT;
    }

    public void setMSG_TEXT(String MSG_TEXT) {
        this.MSG_TEXT = MSG_TEXT;
    }

    public String getWINDOW_TEXT() {
        return WINDOW_TEXT;
    }

    public void setWINDOW_TEXT(String WINDOW_TEXT) {
        this.WINDOW_TEXT = WINDOW_TEXT;
    }
}
